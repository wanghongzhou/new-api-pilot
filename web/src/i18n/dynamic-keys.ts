export type DynamicI18nRegistryName = keyof typeof DYNAMIC_I18N_REGISTRIES

const dynamicRegistrySets = Object.fromEntries(
  Object.entries(DYNAMIC_I18N_REGISTRIES).map(([name, values]) => [
    name,
    new Set<string>(values),
  ])
) as Record<DynamicI18nRegistryName, Set<string>>

export function dynamicI18nKey(
  registryName: DynamicI18nRegistryName,
  key: string
): string {
  if (!dynamicRegistrySets[registryName].has(key)) {
    throw new TypeError(
      `Unregistered dynamic i18n key in ${registryName}: ${key}`
    )
  }
  return key
}
